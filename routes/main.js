/*------------------------------------------------------------------------------------*/
/*  IMPORTACIONES
/*------------------------------------------------------------------------------------*/
var express = require('express');

/*------------------------------------------------------------------------------------*/
/*  INICIALIZACIÓN DE VARIABLES
/*------------------------------------------------------------------------------------*/
var app = express();

/*------------------------------------------------------------------------------------*/
/*  DEFINICION DE RUTAS
/*------------------------------------------------------------------------------------*/
app.get('/', (request, response, next) => {
  response.status(200).json({
    ok: true,
    message: 'Petición exitosa'
  });
});

/*------------------------------------------------------------------------------------*/
/*  EXPORTACIÓN DEL MODELO
/*------------------------------------------------------------------------------------*/
module.exports = app;
