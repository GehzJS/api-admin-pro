/*====================================================================================*/
/*  IMPORTACIONES
/*====================================================================================*/
const express = require('express');
/*====================================================================================*/
/*  INICIALIZACIÓN DE VARIABLES
/*====================================================================================*/
const app = express();
/*====================================================================================*/
/*  DEFINICION DE RUTAS
/*====================================================================================*/
app.get('/', (request, response, next) => {
  response.status(200).json({
    ok: true,
    message: 'Petición exitosa'
  });
});
/*====================================================================================*/
/*  EXPORTACIÓN DEL MODULO
/*====================================================================================*/
module.exports = app;
